        public static async Task SeedAsync(MskContext context, ILoggerFactory loggerFactory, int? retry = 0)
        {
            int retryForAvailability = retry.Value;

            try
            {
                // TODO: Only run this if using a real database
                await context.Database.MigrateAsync();
                await context.Database.EnsureCreatedAsync();

                if (!context.Categories.Any())
                {
                    await context.Categories.AddRangeAsync(GetPreconfiguredCategories());
                    await context.SaveChangesAsync();
                }

                if (!context.Products.Any())
                {
                    await context.Products.AddRangeAsync(GetPreconfiguredProducts());
                    await context.SaveChangesAsync();
                }
            }
            catch (Exception exception)
            {
                if (retryForAvailability < 10)
                {
                    retryForAvailability++;
                    var log = loggerFactory.CreateLogger<MskContextSeed>();
                    log.LogError(exception.Message);
                    await SeedAsync(context, loggerFactory, retryForAvailability);
                }
                throw;
            }
        }

